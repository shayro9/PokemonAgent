def _extract_gen(battle_id: str, log: str) -> int:
    """Infer the generation number from the battle ID or log header.

    :param battle_id: Replay ID, e.g. ``'gen9randombattle-12345'``.
    :param log: Battle log text.
    :returns: Generation number (defaults to 9).
    """
    m = re.match(r"gen(\d+)", battle_id)
    if m:
        return int(m.group(1))
    for line in log.splitlines()[:20]:
        m = re.search(r"\|gen\|(\d+)", line)
        if m:
            return int(m.group(1))
    return 9


def _split_inputlog(inputlog: str) -> tuple[list[str], list[str], list[str]]:
    """Split an inputlog into (p1_requests, p2_requests, choice_lines).

    PS inputlogs have this structure per turn::

        |request|<p1 JSON>
        |request|<p2 JSON>
        <battle events>
        >p1 action
        >p2 action

    :param inputlog: Full inputlog text.
    :returns: Tuple of ``(p1_req_lines, p2_req_lines, choice_lines)`` where
        each is a list of raw text lines in order.
    """
    p1_reqs, p2_reqs, choices = [], [], []
    req_buf: list[str] = []          # accumulate consecutive |request| lines

    for line in inputlog.splitlines():
        line = line.strip()
        if line.startswith("|request|"):
            req_buf.append(line[9:])   # strip "|request|" prefix
        else:
            if req_buf:
                # Flush req_buf: first half → p1, second → p2 (they come in pairs)
                half = len(req_buf) // 2
                p1_reqs.extend(req_buf[:half] if half else req_buf)
                p2_reqs.extend(req_buf[half:] if half else [])
                req_buf = []
            if line.startswith(">p1 ") or line.startswith(">p2 "):
                choices.append(line)

    # Flush any trailing requests
    if req_buf:
        half = len(req_buf) // 2
        p1_reqs.extend(req_buf[:half] if half else req_buf)
        p2_reqs.extend(req_buf[half:] if half else [])

    return p1_reqs, p2_reqs, choices


def _parse_request_json(raw: str) -> dict | None:
    """Parse one request JSON string, returning None on failure."""
    try:
        return json.loads(raw)
    except json.JSONDecodeError:
        return None


def parse_replay_to_samples(
    battle_id: str,
    log: str,
    inputlog: str,
    player: str = "p1",
    winner: str = "",
) -> list[dict]:
    """Parse one replay into a list of ``(obs, action)`` samples.

    :param battle_id: Replay identifier, e.g. ``'gen9randombattle-123'``.
    :param log: Battle log text (the ``log`` field from the replay JSON).
    :param inputlog: Input log text (the ``inputlog`` field from the replay JSON).
    :param player: Which player's perspective to collect: ``'p1'``, ``'p2'``,
        or ``'both'``.
    :param winner: Username of the winning player (stored in metadata).
    :returns: List of sample dicts.
    """
    gen = _extract_gen(battle_id, log)

    # ── Identify player usernames from log ───────────────────────────────
    p1_user, p2_user = "p1user", "p2user"
    for line in log.splitlines()[:10]:
        m = re.match(r"\|player\|p1\|(.+?)\|", line)
        if m:
            p1_user = m.group(1).strip()
        m = re.match(r"\|player\|p2\|(.+?)\|", line)
        if m:
            p2_user = m.group(1).strip()

    # ── Parse inputlog into request queues + choices ─────────────────────
    p1_req_lines, p2_req_lines, choice_lines = _split_inputlog(inputlog)
    p1_req_queue = [_parse_request_json(r) for r in p1_req_lines]
    p2_req_queue = [_parse_request_json(r) for r in p2_req_lines]
    p1_req_queue = [r for r in p1_req_queue if r is not None]
    p2_req_queue = [r for r in p2_req_queue if r is not None]

    # ── Build choice map: turn → {p1: (type, num), p2: (type, num)} ──────
    # Choices come in order; we assign them to turns by counting them
    # Sequence: choices come after the events of the turn they correspond to,
    # but BEFORE the next turn's |turn|N marker in the inputlog.
    # We track them per-turn by pairing them with request queue indices.
    turn_choices: dict[int, dict[str, tuple[str, int]]] = {}

    # We'll match choices to turns during log replay (see below).
    # For now, store them in order to consume sequentially.
    p1_choices: list[tuple[str, int]] = []
    p2_choices: list[tuple[str, int]] = []
    for line in choice_lines:
        parts = line.split()
        if len(parts) < 3:
            continue
        who = parts[0][1:]   # "p1" or "p2"
        act_type = parts[1]
        try:
            act_num = int(parts[2])
        except ValueError:
            continue
        if act_type not in ("move", "switch"):
            continue
        if who == "p1":
            p1_choices.append((act_type, act_num))
        elif who == "p2":
            p2_choices.append((act_type, act_num))

    # ── Create Battle objects for each requested perspective ─────────────
    players_to_collect: list[str] = (
        ["p1", "p2"] if player == "both" else [player]
    )

    battles: dict[str, tuple[Battle, _BattleObserver]] = {}
    for role in players_to_collect:
        username = p1_user if role == "p1" else p2_user
        b = Battle(battle_id, username, logger, gen=gen, save_replays=False)
        obs = _BattleObserver(b, role)
        battles[role] = (b, obs)

    # Feed the first request before any log events
    req_idx = {"p1": 0, "p2": 0}
    if p1_req_queue and "p1" in battles:
        try:
            battles["p1"][0].parse_request(p1_req_queue[0])
            req_idx["p1"] = 1
        except Exception:
            pass
    if p2_req_queue and "p2" in battles:
        try:
            battles["p2"][0].parse_request(p2_req_queue[0])
            req_idx["p2"] = 1
        except Exception:
            pass

    # ── Replay log lines through Battle objects ───────────────────────────
    samples: list[dict] = []
    current_turn = 0
    choice_cursor = {"p1": 0, "p2": 0}

    log_lines = [l.strip() for l in log.splitlines() if l.strip()]

    for raw in log_lines:
        # PS log format: |tag|arg1|arg2|...
        if not raw.startswith("|"):
            continue
        parts = raw.split("|")   # index 0 = "" (prefix), 1 = tag
        if len(parts) < 2:
            continue
        tag = parts[1]

        # ── Terminal: win / tie ───────────────────────────────────────
        if tag in _TERMINAL_TAGS:
            for role, (b, _obs) in battles.items():
                try:
                    if tag == "win":
                        b.won_by(parts[2] if len(parts) > 2 else "")
                    else:
                        b.tied()
                except Exception:
                    pass
            continue

        # ── Turn boundary ─────────────────────────────────────────────
        if tag == "turn":
            new_turn = int(parts[2]) if len(parts) > 2 else current_turn + 1

            # Snapshot obs for the turn that's ENDING (state seen by player)
            # and pair with the choice made on current_turn
            for role, (b, observer) in battles.items():
                ci = choice_cursor[role]
                choices_for_role = p1_choices if role == "p1" else p2_choices
                if ci < len(choices_for_role):
                    act_type, act_num = choices_for_role[ci]
                    action_idx = action_to_idx(act_type, act_num)
                    if action_idx is not None:
                        try:
                            obs_vec = observer.get_obs()
                            samples.append({
                                "obs":         obs_vec.tolist(),
                                "action":      action_idx,
                                "action_type": act_type,
                                "battle_id":   battle_id,
                                "turn":        current_turn,
                                "player":      role,
                                "winner":      winner,
                            })
                        except Exception as exc:
                            logger.debug("obs failed turn %d %s: %s", current_turn, role, exc)
                        observer.set_last_move(act_type, act_num)
                        observer.commit()
                    choice_cursor[role] += 1

            current_turn = new_turn

            # Advance to next turn's request for each player
            for role, (b, _obs) in battles.items():
                req_queue = p1_req_queue if role == "p1" else p2_req_queue
                ri = req_idx[role]
                if ri < len(req_queue):
                    try:
                        b.parse_request(req_queue[ri])
                        req_idx[role] += 1
                    except Exception:
                        req_idx[role] += 1

            # Also send the turn marker to parse_message
            for role, (b, _obs) in battles.items():
                try:
                    b.parse_message(parts)
                except Exception:
                    pass
            continue

        # ── Skip tags that must not reach parse_message ───────────────
        if tag in _SKIP_TAGS or not tag:
            continue

        # ── Normal battle events ──────────────────────────────────────
        for role, (b, _obs) in battles.items():
            try:
                b.parse_message(parts)
            except NotImplementedError:
                pass   # unrecognised events are safe to skip
            except Exception as exc:
                logger.debug("parse_message error (%s, turn %d): %s", tag, current_turn, exc)

    return samples


# ---------------------------------------------------------------------------
# Dataset builder (over many replays)
# ---------------------------------------------------------------------------

def build_dataset(
    replay_jsonl: Path,
    out_path: Path,
    player: str = "p1",
    stats_only: bool = False,
) -> None:
    """Convert all replays in a JSONL file to (obs, action) samples.

    :param replay_jsonl: Path to JSONL file where each line is a replay dict
        with at least ``battle_id``, ``log``, and ``inputlog`` fields.
    :param out_path: Output JSONL path (one sample per line).
    :param player: ``'p1'``, ``'p2'``, or ``'both'``.
    :param stats_only: If ``True``, count and print stats without writing.
    """
    if not replay_jsonl.exists():
        sys.exit(f"[ERROR] Replay file not found: {replay_jsonl}")

    out_path.parent.mkdir(parents=True, exist_ok=True)

    total_replays = 0
    skipped_no_inputlog = 0
    skipped_error = 0
    total_samples = 0
    action_counts = {"move": 0, "switch": 0}

    out_fh = None if stats_only else out_path.open("w", encoding="utf-8")

    try:
        with replay_jsonl.open(encoding="utf-8") as fh:
            for line_no, line in enumerate(fh, 1):
                line = line.strip()
                if not line:
                    continue

                try:
                    replay = json.loads(line)
                except json.JSONDecodeError:
                    logger.warning("Line %d: invalid JSON — skipping", line_no)
                    skipped_error += 1
                    continue

                battle_id = replay.get("battle_id") or replay.get("id", f"replay-{line_no}")
                log       = replay.get("log", "")
                inputlog  = replay.get("inputlog", "")
                winner    = replay.get("winner", "")

                total_replays += 1

                if not inputlog:
                    skipped_no_inputlog += 1
                    continue

                if not log:
                    skipped_error += 1
                    continue

                try:
                    samples = parse_replay_to_samples(
                        battle_id=battle_id,
                        log=log,
                        inputlog=inputlog,
                        player=player,
                        winner=winner,
                    )
                except Exception as exc:
                    logger.warning("Failed to parse %s: %s", battle_id, exc)
                    skipped_error += 1
                    continue

                for sample in samples:
                    action_counts[sample["action_type"]] += 1
                    total_samples += 1
                    if out_fh is not None:
                        out_fh.write(json.dumps(sample) + "\n")

                if total_replays % 100 == 0:
                    print(
                        f"  [{total_replays}] replays processed | "
                        f"samples: {total_samples} "
                        f"(moves={action_counts['move']}, "
                        f"switches={action_counts['switch']})"
                    )
    finally:
        if out_fh is not None:
            out_fh.close()

    print(f"\n{'─'*55}")
    print(f"Replays read    : {total_replays}")
    print(f"No inputlog     : {skipped_no_inputlog}  ← use gen9randombattle format")
    print(f"Parse errors    : {skipped_error}")
    print(f"Total samples   : {total_samples}")
    print(f"  Move samples  : {action_counts['move']}")
    print(f"  Switch samples: {action_counts['switch']}")
    if not stats_only:
        print(f"Output          : {out_path}")
    print(f"{'─'*55}")


# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------

def _build_parser() -> argparse.ArgumentParser:
    p = argparse.ArgumentParser(
        description="Build supervised (obs, action) dataset from PS replay inputlogs.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__,
    )
    p.add_argument(
        "--replay-jsonl",
        required=True,
        type=Path,
        help="Path to JSONL file of replays (each line must have 'log' + 'inputlog' fields).",
    )
    p.add_argument(
        "--out-path",
        default="data/supervised/dataset.jsonl",
        type=Path,
        help="Output JSONL path (default: data/supervised/dataset.jsonl).",
    )
    p.add_argument(
        "--player",
        default="p1",
        choices=["p1", "p2", "both"],
        help="Whose perspective to extract: p1, p2, or both (default: p1).",
    )
    p.add_argument(
        "--stats-only",
        action="store_true",
        help="Only print statistics, do not write output file.",
    )
    return p


def main() -> None:
    args = _build_parser().parse_args()
    build_dataset(
        replay_jsonl=args.replay_jsonl,
        out_path=args.out_path,
        player=args.player,
        stats_only=args.stats_only,
    )


if __name__ == "__main__":
    main()
