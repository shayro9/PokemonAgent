"""
build_supervised_dataset.py
============================
Convert Pokémon Showdown replay inputlogs into (obs, action) supervised
learning pairs using the same BattleState / BattleTracker / reward stack
as the RL environment.

HOW IT WORKS
------------
A PS inputlog contains, interleaved:

    |request|<JSON>          ← server sends available moves + team state
    |<battle event>|...      ← turn events (moves, switches, damage, …)
    >p1 move N               ← player's choice (1-indexed)
    >p2 switch M             ← opponent's choice

We replay the log through a real poke-env ``Battle`` object:

    1.  Feed ``|request|`` → ``battle.parse_request()``
        → populates ``available_moves`` and ``available_switches``
    2.  Feed battle events   → ``battle.parse_message()``
        → advances HP, boosts, status, weather, …
    3.  At each ``|turn|N`` boundary, snapshot the observation BEFORE
        announcing the new turn (i.e. state the player sees when choosing)
        and pair it with the ``>p1 action`` choice for that turn.

Action encoding (matches your existing RL wrapper):
    0–5   switch to party slot 0–5
    6–9   use move slot 0–3

OUTPUT
------
Each sample is a dict:
    {
        "obs":        list[float],   # 383-dim BattleState vector
        "action":     int,           # 0-9
        "action_type": str,          # "move" or "switch"
        "battle_id":  str,
        "turn":       int,
        "player":     str,           # "p1" or "p2"
        "winner":     str,
    }

USAGE
-----
# Scrape replays first (requires internet — run locally):
#   python scrape_replays.py --format gen9randombattle --num-replays 2000

# Build dataset from JSONL of replays (each line has "log" + "inputlog"):
python build_supervised_dataset.py \\
    --replay-jsonl data/replays/raw_battles.jsonl \\
    --out-path    data/supervised/dataset.jsonl \\
    --player      p1          # p1 | p2 | both (default: p1)

# Inspect stats only:
python build_supervised_dataset.py --replay-jsonl data/replays/raw_battles.jsonl --stats-only

NOTES
-----
* The inputlog is only available for formats with autogenerated teams
  (gen9randombattle, gen9doublesrandombattle, …).  Ladder replays of
  standard formats (OU, UU, …) do NOT include the inputlog by default
  and cannot be used here.
* Replays with unparseable logs are skipped with a warning counter.
* gen9customgame replays (your training format) also work if the server
  saved an inputlog.
"""

