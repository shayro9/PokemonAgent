# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------
# Tags that parse_message handles via dedicated methods (not raise NotImplementedError)
_TERMINAL_TAGS = {"win", "tie"}
# Tags that need special routing (not passed to parse_message)
_SKIP_TAGS = {
    # already in MESSAGES_TO_IGNORE, but also things we must not raise on
    "request",   # handled separately
    "t:",        # timestamp
    "inactive",
    "inactiveoff",
}


# ---------------------------------------------------------------------------
# Action encoding
# ---------------------------------------------------------------------------

def action_to_idx(action_type: str, action_num: int) -> int | None:
    """Convert a ``>pN move/switch X`` line into a 0-based action index.

    :param action_type: ``'move'`` or ``'switch'``.
    :param action_num: 1-based slot number from the inputlog.
    :returns: Integer in ``[0, 9]``, or ``None`` if the value is out of range.
    """
    if action_type == "switch":
        idx = action_num - 1     # 0-5
        return idx if 0 <= idx <= 5 else None
    elif action_type == "move":
        idx = 6 + (action_num - 1)   # 6-9
        return idx if 6 <= idx <= 9 else None
    return None


# ---------------------------------------------------------------------------
# Per-battle observation builder
# ---------------------------------------------------------------------------

class _BattleObserver:
    """Maintains BattleTracker state alongside a poke-env Battle."""

    def __init__(self, battle: Battle, player_role: str):
        self.battle = battle
        self.player_role = player_role
        self.tracker = BattleTracker()

    def update_trackers(self) -> None:
        """Update stat/protect beliefs from current battle state."""
        if not HAS_PROJECT:
            return
        b = self.battle
        opp = b.opponent_active_pokemon
        if opp is None:
            return

        # Detect opponent move + no-damage via event parser
        opp_last_move = detect_opponent_move_from_events(b)
        no_damage = did_no_damage_from_events(b, self.tracker.my_last_move)

        # Protect belief
        protected = (
            opp_last_move.is_protect_move
            if opp_last_move
            else (None if no_damage else False)
        )
        prior = estimate_protect_attempt_prior(b)
        self.tracker.last_opp_pp = snapshot_opponent_pp(b)

        if self.tracker.my_last_move is not None:
            belief = build_protect_belief(
                self.tracker.my_last_move,
                self.tracker.last_protect_chance,
                protected,
                prior,
            )
            self.tracker.last_protect_chance = belief.expected_next_protect_chance()
            self.tracker.protect_belief = belief.expected_next_protect_belief()
        else:
            self.tracker.protect_belief = prior

        # Stat belief
        if self.tracker.stat_belief is None:
            self.tracker.stat_belief = build_stat_belief(opp, b.gen)
        else:
            self.tracker.stat_belief = update_stat_belief(
                self.tracker.stat_belief, b, self.tracker, opp_last_move
            )

    def get_obs(self) -> np.ndarray:
        """Build the 383-dim observation vector for the current battle state."""
        if not HAS_PROJECT:
            return np.zeros(OBS_SIZE if HAS_PROJECT else 383, dtype=np.float32)
        self.update_trackers()
        stat_vec = (
            self.tracker.stat_belief.to_array()
            if self.tracker.stat_belief is not None
            else None
        )
        return BattleState.from_battle(
            self.battle,
            opp_protect_belief=self.tracker.protect_belief,
            opp_stat_belief=stat_vec,
            stat_belief_obj=self.tracker.stat_belief,
        ).to_array()

    def commit(self) -> None:
        """Snapshot HP/status at end of turn (after reward-reading)."""
        self.tracker.commit(self.battle)

    def set_last_move(self, action_type: str, action_num: int) -> None:
        """Record the move we played so the next turn's belief update is correct."""
        if action_type == "move":
            idx = action_num - 1
            moves = self.battle.available_moves
            if idx < len(moves):
                self.tracker.my_last_move = moves[idx]
            else:
                self.tracker.my_last_move = None
        else:
            self.tracker.my_last_move = None


